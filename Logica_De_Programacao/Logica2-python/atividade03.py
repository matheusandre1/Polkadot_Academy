num1 = int(input("Digite um numero: "))
num2 = int(input("Digite outro numero: "))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print("A soma dos dois numeros é: ", soma)
print("A subtração dos dois numeros é: ", subtracao)
print("A multiplicação dos dois numeros é: ", multiplicacao)
print("A divisão dos dois numeros é: ", divisao)