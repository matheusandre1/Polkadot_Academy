num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

operation = input("Qual Operação Matematica ?")

if operation == "+":
    print(num1 + num2)
elif operation == "-":
    print(num1 - num2)
elif operation == "/":
    print(num1 / num2)
elif operation == "*":
    print(num1 * num2)